@dp.message()
async def handle_text(message: types.Message):
    text = message.text.strip()
    
    if not text:
        await message.answer("❌ Отправь текст с VLESS-ссылками")
        return
    
    if "vless://" not in text:
        await message.answer("❌ Не найдено VLESS-ссылок в сообщении")
        return
    
    # Находим все VLESS-ссылки в тексте
    vless_pattern = r"vless://[^\s]+"
    links = re.findall(vless_pattern, text)
    
    if not links:
        await message.answer("❌ Не найдено VLESS-ссылок")
        return
    
    # Обрабатываем каждую ссылку
    result_lines = []
    for idx, link in enumerate(links, 1):
        processed = process_single_link(link, idx)
        result_lines.append(processed)
    
    result_text = '\n'.join(result_lines)
    count = len(links)
    
    # Решаем: отправлять текстом или файлом
    MAX_LINKS_FOR_TEXT = 5
    
    if count <= MAX_LINKS_FOR_TEXT:
        # Мало ссылок — отправляем текстом (кликабельно)
        word = "ссылка" if count == 1 else "ссылки" if count <= 4 else "ссылок"
        await message.answer(
            f"✅ *Обработано {count} {word}*\n\n"
            f"{result_text}",
            parse_mode="Markdown",
            disable_web_page_preview=True
        )
    else:
        # Много ссылок — отправляем файлом
        result_bytes = result_text.encode('utf-8')
        await message.answer_document(
            types.BufferedInputFile(result_bytes, filename="formatted_links.txt"),
            caption=f"✅ Обработано ссылок: {count}\n📎 Результат в файле (ссылок много, в сообщение не помещаются)"
        )

